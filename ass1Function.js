// Write a function that sums two number
function sum (a,b){
	return a+b
}
	console.log(sum(10,5))

// Write a function that substract two number
function sum (a,b){
	return a-b
}
	console.log(sum(10,5))

// Write a function that divides two number
function sum (a,b){
	return a/b
}
	console.log(sum(10,5))

// Write a function that multiply two number
function sum (a,b){
	return a*b
}
	console.log(sum(10,5))

//Using the arrow function
//A arrow function to sums two number
const sumAr1 = (a,b)=>a+b
console.log(sumAr1(10,5))
//A arrow function to subtract two number

const sumAr2 = (a,b)=>a-b
console.log(sumAr2(10,5))

//A arrow function to divide two number
const sumAr3 = (a,b)=>a/b
console.log(sumAr3(10,5))

//A arrow function to multiply two number
const sumAr4 = (a,b)=>a*b
console.log(sumAr4(10,5))